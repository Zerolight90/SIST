package am;

public class Ex2_Member {
	// 필요한 정보들을 맴버필드로 선언한다.
	private String name, phone;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getPhone() {
		return phone;
	}

	public void setPhone(String phone) {
		this.phone = phone;
	}
	

	

}
